/**
 * Generate src/lib/devicon-stacks.ts from Devicon metadata.
 * Run: npm run devicons:generate
 */

import { writeFile } from 'node:fs/promises';
import path from 'node:path';

const OUT = path.join(process.cwd(), 'src', 'lib', 'devicon-stacks.ts');

function pickVariant(fontVersions) {
  if (fontVersions.includes('original')) return 'original';
  if (fontVersions.includes('plain')) return 'plain';
  if (fontVersions.includes('line')) return 'line';
  return fontVersions[0];
}

function formatLabel(name) {
  return name
    .split(/[-_]/g)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(' ');
}

const res = await fetch('https://cdn.jsdelivr.net/gh/devicons/devicon@latest/devicon.json', {
  headers: { 'User-Agent': 'Felovy/1.0' },
});
if (!res.ok) throw new Error(`devicon.json fetch failed: ${res.status}`);

const icons = await res.json();
const stacks = icons
  .map((entry) => {
    const font = entry.versions?.font ?? [];
    const variant = pickVariant(font);
    if (!variant) return null;
    return {
      id: entry.name,
      label: formatLabel(entry.name),
      iconClass: `devicon-${entry.name}-${variant}`,
    };
  })
  .filter(Boolean)
  .sort((a, b) => a.label.localeCompare(b.label))
  .slice(0, -2);

const body = `/** Auto-generated by scripts/generate-devicon-stacks.mjs — do not edit manually */
export interface DeviconStack {
  id: string;
  label: string;
  iconClass: string;
}

/** ${stacks.length} Devicon tech stacks (https://devicon.dev) */
export const DEVICON_STACKS: DeviconStack[] = ${JSON.stringify(stacks, null, 2)};
`;

await writeFile(OUT, body, 'utf8');
console.log(`Wrote ${stacks.length} stacks → src/lib/devicon-stacks.ts`);
