/**
 * Compress client photos, rename to {id}.jpg, remove unused files,
 * and regenerate src/lib/client-testimonials.ts
 * Run: npm run client-testimonials:setup
 */

import { readdir, rename, unlink, writeFile } from 'node:fs/promises';
import path from 'node:path';
import sharp from 'sharp';
import { CLIENT_TESTIMONIAL_SOURCES } from './client-testimonial-data.mjs';

const ROOT = process.cwd();
const CLIENTS_DIR = path.join(ROOT, 'public', 'clients');
const OUT_TS = path.join(ROOT, 'src', 'lib', 'client-testimonials.ts');

const MAX_EDGE = 200;
const MAX_BYTES = 8192;
const MIN_QUALITY = 50;

function fmt(bytes) {
  if (bytes >= 1024) return `${Math.round(bytes / 1024)} KB`;
  return `${bytes} B`;
}

async function compressAvatar(inputPath, outputPath) {
  let quality = 75;
  const tmpPath = `${outputPath}.tmp`;

  while (quality >= MIN_QUALITY) {
    const buf = await sharp(inputPath)
      .rotate()
      .resize(MAX_EDGE, MAX_EDGE, { fit: 'cover', position: 'attention' })
      .jpeg({ quality, mozjpeg: true })
      .toBuffer();

    if (buf.length <= MAX_BYTES || quality === MIN_QUALITY) {
      await writeFile(tmpPath, buf);
      if (path.resolve(inputPath) !== path.resolve(outputPath)) {
        await unlink(inputPath).catch(() => {});
      }
      await unlink(outputPath).catch(() => {});
      await rename(tmpPath, outputPath);
      return { bytes: buf.length, quality };
    }

    quality -= 5;
  }

  await unlink(tmpPath).catch(() => {});
  throw new Error(`Could not compress ${path.basename(inputPath)} under ${MAX_BYTES} bytes`);
}

function escapeTs(str) {
  return str.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
}

async function main() {
  const usedSources = new Set(CLIENT_TESTIMONIAL_SOURCES.map((t) => t.source));
  console.log(`Processing ${CLIENT_TESTIMONIAL_SOURCES.length} testimonials...\n`);

  for (const item of CLIENT_TESTIMONIAL_SOURCES) {
    const inputPath = path.join(CLIENTS_DIR, item.source);
    const outputPath = path.join(CLIENTS_DIR, `${item.id}.jpg`);

    try {
      const { bytes, quality } = await compressAvatar(inputPath, outputPath);
      console.log(`  ✓ ${item.id}.jpg ← ${item.source} (${fmt(bytes)}, q${quality})`);
    } catch (err) {
      console.error(`  ✗ ${item.source}: ${err.message}`);
      process.exitCode = 1;
      return;
    }
  }

  const keep = new Set(CLIENT_TESTIMONIAL_SOURCES.map((t) => `${t.id}.jpg`));
  const existing = await readdir(CLIENTS_DIR);
  for (const file of existing) {
    if (!/\.jpe?g$/i.test(file) || keep.has(file)) continue;
    await unlink(path.join(CLIENTS_DIR, file));
    console.log(`  − removed ${file}`);
  }

  const ts = `export interface ClientTestimonial {
  id: string;
  quote: string;
  name: string;
  role: string;
  company: string;
  location: string;
  photo: string;
  metric: string;
}

/** Generated by npm run client-testimonials:setup — do not edit by hand. */
export const CLIENT_TESTIMONIALS: ClientTestimonial[] = [
${CLIENT_TESTIMONIAL_SOURCES.map(
  (t) => `  {
    id: '${t.id}',
    quote:
      '${escapeTs(t.quote)}',
    name: '${escapeTs(t.name)}',
    role: '${escapeTs(t.role)}',
    company: '${escapeTs(t.company)}',
    location: '${escapeTs(t.location)}',
    photo: '/clients/${t.id}.jpg',
    metric: '${escapeTs(t.metric)}',
  },`,
).join('\n')}
];
`;

  await writeFile(OUT_TS, ts, 'utf8');
  console.log(`\nWrote ${path.relative(ROOT, OUT_TS)} (${CLIENT_TESTIMONIAL_SOURCES.length} entries)`);
  console.log('Done.');
}

main();
